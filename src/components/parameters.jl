
"""
Gets a pairwise parameter from the parameter set if it is there.
Otherwise, just returns 0.0 and false.
"""
@inline function maybe_get_pairwise_parameter(label_1::String, label_2::String, param_suffix::Symbol, params::Dict{Symbol, Float64})
    sym_ij = Symbol(label_1, label_2, param_suffix)
    if haskey(params, sym_ij)
        return (params[sym_ij], true)
    end
    sym_ji = Symbol(label_2, label_1, param_suffix)
    if haskey(params, sym_ji)
        return (params[sym_ji], true)
    end
    return (0.0, false)
end

"""
Gets a pairwise parameter from the parameter set if it is there.
Otherwise, just returns 0.0 and false. This version only checks the order
of labels passed in.
"""
@inline function maybe_get_asymmetric_pairwise_parameter(label_1::String, label_2::String, param_suffix::Symbol, params::Dict{Symbol, Float64})
    sym_ij = Symbol(label_1, label_2, param_suffix)
    if haskey(params, sym_ij)
        return (params[sym_ij], true)
    end
    return (0.0, false)
end

@inline function maybe_get_param_or_return_default(param_name::Symbol, params::Dict{Symbol, Float64}, default::Float64=0.0)
    if haskey(params, param_name)
        return (params[param_name], true)
    end
    return (default, false)
end

